async def _create_course_and_lesson(async_client, headers: dict[str, str]) -> tuple[str, str]:
    slug = f"logs-mcp-course-{uuid.uuid4().hex[:6]}"
    course_resp = await async_client.post(
        "/studio/courses",
        headers=headers,
        json={
            "title": "Logs MCP Course",
            "slug": slug,
            "course_group_id": str(uuid.uuid4()),
            "group_position": 0,
            "price_amount_cents": None,
            "drip_enabled": False,
            "drip_interval_days": None,
        },
    )
    assert course_resp.status_code == 200, course_resp.text
    course_id = str(course_resp.json()["id"])

    lesson_resp = await async_client.post(
        f"/studio/courses/{course_id}/lessons",
        headers=headers,
        json={
            "lesson_title": "Logs Lesson",
            "position": 1,
        },
    )
    assert lesson_resp.status_code == 200, lesson_resp.text
    lesson_id = str(lesson_resp.json()["id"])
    return course_id, lesson_id


async def _ensure_media_resolution_failures_table() -> None:
    from app import db

    async with db.pool.connection() as conn:  # type: ignore[attr-defined]
        async with conn.cursor() as cur:  # type: ignore[attr-defined]
            await cur.execute(
                """
                CREATE TABLE IF NOT EXISTS app.media_resolution_failures (
                  id bigint generated by default as identity primary key,
                  created_at timestamptz not null default now(),
                  lesson_media_id uuid references app.lesson_media(id) on delete set null,
                  mode text not null,
                  reason text not null,
                  details jsonb not null default '{}'::jsonb
                )
                """
            )
            await cur.execute(
                """
                ALTER TABLE app.lesson_media
                  ADD COLUMN IF NOT EXISTS is_test boolean not null default false
                """
            )
            await cur.execute(
                """
                ALTER TABLE app.lesson_media
                  ADD COLUMN IF NOT EXISTS test_session_id uuid
                """
            )
            await cur.execute(
                """
                ALTER TABLE app.lessons
                  ADD COLUMN IF NOT EXISTS is_test boolean not null default false
                """
            )
            await cur.execute(
                """
                ALTER TABLE app.lessons
                  ADD COLUMN IF NOT EXISTS test_session_id uuid
                """
            )
            await cur.execute(
                """
                CREATE OR REPLACE FUNCTION app.is_test_row_visible(
                  row_is_test boolean,
                  row_test_session_id uuid
                )
                RETURNS boolean
                LANGUAGE sql
                STABLE
                AS $$
                  SELECT CASE
                    WHEN COALESCE(row_is_test, false) = false THEN true
                    WHEN NULLIF(current_setting('app.test_session_id', true), '') IS NULL
                      THEN false
                    ELSE row_test_session_id =
                      NULLIF(current_setting('app.test_session_id', true), '')::uuid
                  END
                $$
                """
            )
            await conn.commit()


