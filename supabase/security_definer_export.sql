    return false;
  end if;

  return (
    exists(
      select 1
      from app.seminars s
      where s.id = p_seminar_id
        and s.host_id = target_user
    )
    or exists(
      select 1
      from app.seminar_attendees sa
      where sa.seminar_id = p_seminar_id
        and sa.user_id = target_user
    )
  );
end;
